"""
Operações Matématicas em Python
"""

soma = 5 + 10
print("Soma: ", soma)

subt = 5 - 10
print("Subtração: ", subt)

div = 10 / 3
print("Divisão: ", div)

div_int = 10 // 3
print("Divisão Inteira: ", div_int)

div_rest = 10 % 3
print("Divisão Resto: ", div_rest)

mult = 10 * 3
print("Multiplicação: ", mult)

potencia = 10 ** 3
print("Potencia: ", potencia)